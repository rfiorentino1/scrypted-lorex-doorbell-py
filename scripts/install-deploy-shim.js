#!/usr/bin/env node
// Replace node_modules/.bin/scrypted-deploy with a wrapper that runs
// scrypted-webpack before scrypted-deploy. Invoked from package.json's
// "postinstall" hook so the wrapper survives `npm install`.
//
// Why this exists: npm pre/post-script lifecycle hooks (prescrypted-deploy
// etc.) only fire on `npm run X`, not on `npx X` or direct invocation of
// the bin. `npx scrypted-deploy` therefore skips webpack and ships
// whatever stale `out/plugin.zip` is sitting around — which is what
// silently shipped the old plugin code on 2026-05-05 and required this
// fix in the first place.
//
// Replacing the bin entry catches every invocation path:
//   `npx scrypted-deploy <ip>`
//   `node_modules/.bin/scrypted-deploy <ip>`
//   `npm run scrypted-deploy <ip>` (also covered by prescrypted-deploy hook)
//
// Idempotent: replaces the entry whether it's currently a symlink (fresh
// npm install) or a previous copy of this wrapper. No-op if node_modules
// hasn't been installed yet.

'use strict';

const fs = require('fs');
const path = require('path');

const projectRoot = path.resolve(__dirname, '..');
const binDir = path.join(projectRoot, 'node_modules', '.bin');
const target = path.join(binDir, 'scrypted-deploy');

if (!fs.existsSync(binDir)) {
    console.log('[install-deploy-shim] node_modules/.bin not found; skipping');
    process.exit(0);
}

const wrapper = `#!/usr/bin/env node
// Auto-generated by scripts/install-deploy-shim.js. DO NOT EDIT here;
// edit the source script and re-run \`npm install\` (or \`node
// scripts/install-deploy-shim.js\` directly) to regenerate.
//
// Runs scrypted-webpack before scrypted-deploy so direct invocations
// (npx scrypted-deploy, ./node_modules/.bin/scrypted-deploy) which bypass
// npm lifecycle hooks still ship fresh code.

'use strict';

const { spawnSync } = require('child_process');
const path = require('path');

// __dirname is node_modules/.bin/ — we are written there as a real file
// (not a symlink), so __dirname resolves correctly without symlink
// chasing. SDK bin scripts live one level up + into @scrypted/sdk.
const sdkBinDir = path.join(__dirname, '..', '@scrypted', 'sdk', 'dist', 'src', 'bin');

function run(jsPath, args) {
    const r = spawnSync(process.execPath, [jsPath].concat(args || []), { stdio: 'inherit' });
    if (r.error) { console.error(r.error); process.exit(1); }
    if (r.status !== 0) process.exit(r.status);
}

run(path.join(sdkBinDir, 'scrypted-webpack.js'));
run(path.join(sdkBinDir, 'scrypted-deploy.js'), process.argv.slice(2));
`;

// Remove whatever is currently there (symlink, file, or nothing). We do
// not care which — we want our wrapper to win.
try {
    fs.unlinkSync(target);
} catch (e) {
    if (e.code !== 'ENOENT') throw e;
}

fs.writeFileSync(target, wrapper, { mode: 0o755 });

console.log('[install-deploy-shim] installed wrapper at',
    path.relative(projectRoot, target));
